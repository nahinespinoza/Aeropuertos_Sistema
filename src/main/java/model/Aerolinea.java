package model;

/**
 *
 * @author LENOVO
 */
public class Aerolinea {
    
    private String code;  
    private String name;      
    private String pais;  

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public Aerolinea(String code, String name, String pais) {
        this.code = code;
        this.name = name;
        this.pais = pais;
    }
    
    
    
    
}
